/**
 * Extracts SDK versions from the per-platform getting started guides and emits
 * a TypeScript constants file consumed by app/components/onboarding.tsx.
 *
 * Source of truth: content/docs/getting-started/<platform>.mdx (each kept up
 * to date by the per-platform release prep workflows in .github/workflows/).
 *
 * Runs in the prebuild/dev/build/pretest chains, so the onboarding snippets
 * always show versions matching the rendered docs.
 */
const fs = require("fs");
const path = require("path");

const SCRIPT_DIR = __dirname;
const DASHBOARD_ROOT = path.join(SCRIPT_DIR, "..");
const GETTING_STARTED_DIR = path.join(
  DASHBOARD_ROOT,
  "content",
  "docs",
  "getting-started",
);
const OUTPUT_FILE = path.join(
  DASHBOARD_ROOT,
  "app",
  "components",
  "sdk_versions.generated.ts",
);

// Each version string lives in its own platform page. `file` is the page the
// first matching occurrence must appear in; `pattern` captures the version.
const SOURCES = {
  androidSdk: {
    file: "android.mdx",
    pattern: /measure-android:(\d+\.\d+\.\d+)/,
  },
  iosSdk: {
    file: "ios.mdx",
    pattern: /branch:\s*["']ios-v(\d+\.\d+\.\d+)["']/,
  },
  flutter: {
    file: "flutter.mdx",
    pattern: /measure_flutter:\s*\^(\d+\.\d+\.\d+)/,
  },
  reactNative: {
    file: "react-native.mdx",
    pattern: /@measuresh\/react-native@(\d+\.\d+\.\d+)/,
  },
  kmp: {
    file: "kotlin-multiplatform.mdx",
    pattern: /measure-kmp:(\d+\.\d+\.\d+)/,
  },
};

function extract(sources = SOURCES) {
  const out = {};
  for (const [key, { file, pattern }] of Object.entries(sources)) {
    const filePath = path.join(GETTING_STARTED_DIR, file);
    if (!fs.existsSync(filePath)) {
      throw new Error(
        `Could not find the getting started guide for ${key} at ${filePath}`,
      );
    }
    const content = fs.readFileSync(filePath, "utf-8");
    const match = content.match(pattern);
    if (!match) {
      throw new Error(
        `Could not extract ${key} version from ${path.relative(
          DASHBOARD_ROOT,
          filePath,
        )} (pattern: ${pattern})`,
      );
    }
    out[key] = match[1];
  }
  return out;
}

function generate(versions) {
  return `// Generated by scripts/extract_sdk_versions.js — do not edit by hand.
// Sourced from content/docs/getting-started/*.mdx at build time.

export const SDK_VERSIONS = {
  androidSdk: ${JSON.stringify(versions.androidSdk)},
  iosSdk: ${JSON.stringify(versions.iosSdk)},
  flutter: ${JSON.stringify(versions.flutter)},
  reactNative: ${JSON.stringify(versions.reactNative)},
  kmp: ${JSON.stringify(versions.kmp)},
} as const
`;
}

if (require.main === module) {
  console.log(
    `Extracting SDK versions from ${path.relative(
      DASHBOARD_ROOT,
      GETTING_STARTED_DIR,
    )}/...`,
  );
  const versions = extract();
  fs.writeFileSync(OUTPUT_FILE, generate(versions));
  console.log(
    `Wrote SDK versions to ${path.relative(DASHBOARD_ROOT, OUTPUT_FILE)}:`,
  );
  for (const [k, v] of Object.entries(versions)) {
    console.log(`  ${k}: ${v}`);
  }
}

module.exports = { extract, generate, SOURCES };
